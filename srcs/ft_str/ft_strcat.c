char	*ft_strcat(char *s1, const char *s2)
{
	char	*ptr;

	ptr = s1;
	while (ptr && *ptr)
		ptr++;
	while (ptr && s2 && *s2)
		*(ptr++) = *(s2++);
	*ptr = '\0';
	return (s1);
}
